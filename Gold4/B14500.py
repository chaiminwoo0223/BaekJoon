import sys

input = sys.stdin.readline

n, m = map(int, input().split())
papers = [list(map(int, input().split())) for _ in range(n)]
k = 0
result = 0

boxes = [
    # I
    [(0, 0),(0, 1),(0, 2),(0, 3)],
    [(0, 0),(1, 0),(2, 0),(3, 0)],
    # O
    [(0, 0),(0, 1),(1, 0),(1, 1)],
    # L (8)
    [(0, 0),(1, 0),(2, 0),(2, 1)],
    [(0, 1),(1, 1),(2, 1),(2, 0)],
    [(0, 0),(0, 1),(1, 0),(2, 0)],
    [(0, 0),(0, 1),(1, 1),(2, 1)],
    [(0, 0),(0, 1),(0, 2),(1, 0)],
    [(0, 0),(0, 1),(0, 2),(1, 2)],
    [(0, 0),(1, 0),(1, 1),(1, 2)],
    [(0, 2),(1, 0),(1, 1),(1, 2)],
    # S/Z (4)
    [(0, 0),(0, 1),(1, 1),(1, 2)],
    [(0, 1),(0, 2),(1, 0),(1, 1)],
    [(0, 1),(1, 0),(1, 1),(2, 0)],
    [(0, 0),(1, 0),(1, 1),(2, 1)],
    # T (4)
    [(0, 0),(0, 1),(0, 2),(1, 1)],
    [(0, 1),(1, 0),(1, 1),(2, 1)],
    [(0, 1),(1, 0),(1, 1),(1, 2)],
    [(0, 0),(1, 0),(1, 1),(2, 0)],
]

def calculator(x, y, z):
    for box in boxes:
        t = 0

        for w in range(4):
            if 0 <= x + box[w][0] < n and 0 <= y + box[w][1] < m:
                t += papers[x + box[w][0]][y + box[w][1]]
            else:
                t = 0
                break

        z = max(z, t)

    return z

for i in range(n):
    for j in range(m):
        k = calculator(i, j, k)

print(k)
